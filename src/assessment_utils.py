def _compute_vector_cover_percentage(vector_gdf, boundary_gdf):
    """Internally calculates the base % cover of a vector within a geographic boundary."""
    vector_gdf = vector_gdf.to_crs(boundary_gdf.crs)
    clipped_vector = gpd.clip(vector_gdf, boundary_gdf)

    total_feature_area = clipped_vector.geometry.area.sum()
    total_boundary_area = boundary_gdf.geometry.area.sum()

    return (total_feature_area / total_boundary_area) * 100


def _get_vector_vs_raster_errors(vector_gdf, labelled_tiles, target_raster_vals, testing_window=None):
    """
    Internally calculates bias errors and patch areas dynamically.
    If testing_window is provided, only that specific pixel strip is evaluated.
    """
    errors = []
    patch_areas_ha = []

    for tile_path in labelled_tiles:
        with rasterio.open(tile_path) as src:
            # 1. Read ground truth and define patch bounds
            if testing_window:
                l_data = src.read(1, window=testing_window)
                patch_geom = box(*src.window_bounds(testing_window))
            else:
                l_data = src.read(1)
                patch_geom = box(*src.bounds)

            # 2. Area in Hectares
            patch_area = patch_geom.area / 10000.0
            patch_areas_ha.append(patch_area)

            # 3. Ground Truth %
            true_pct = np.isin(l_data, target_raster_vals).mean() * 100

            # 4. Model % (Intersect Vector with the Patch Bounding Box)
            patch_gdf = gpd.GeoDataFrame({'geometry': [patch_geom]}, crs=src.crs)
            vector_gdf = vector_gdf.to_crs(src.crs)
            clipped_features = gpd.clip(vector_gdf, patch_gdf)

            model_pct = (clipped_features.geometry.area.sum() / patch_geom.area) * 100

            # 5. Error
            errors.append(model_pct - true_pct)

    return np.array(errors), np.array(patch_areas_ha)


def evaluate_final_cover_and_errors(canopy_vector_path, green_vector_path, boundary_vector_path,
                                    labelled_tiles, plot_output_path, testing_window=None):
    """
    Master function: Computes final cover %, extracts errors, calculates SE, and plots convergence.
    """
    # 1. Load Data
    canopy_gdf = gpd.read_file(canopy_vector_path)
    green_gdf = gpd.read_file(green_vector_path)
    boundary_gdf = gpd.read_file(boundary_vector_path)

    # 2. Compute Base Cover Percentages
    c_base_cover = _compute_vector_cover_percentage(canopy_gdf, boundary_gdf)
    g_base_cover = _compute_vector_cover_percentage(green_gdf, boundary_gdf)

    # 3. Calculate Errors
    c_errs, c_areas = _get_vector_vs_raster_errors(canopy_gdf, labelled_tiles, [1], testing_window)
    g_errs, g_areas = _get_vector_vs_raster_errors(green_gdf, labelled_tiles, [1, 2], testing_window)

    # 4. Convergence Statistics
    def get_stats(errors):
        means = [np.mean(errors[:i]) for i in range(1, len(errors) + 1)]
        stds = [np.std(errors[:i]) for i in range(1, len(errors) + 1)]
        rms = [np.sqrt(np.mean(errors[:i] ** 2)) for i in range(1, len(errors) + 1)]
        return means, stds, rms

    c_means, c_stds, c_rms = get_stats(c_errs)
    g_means, g_stds, g_rms = get_stats(g_errs)

    # 5. Calculate Standard Error (SE)
    total_area_ha = boundary_gdf.geometry.area.sum() / 10000.0
    avg_patch_ha = np.mean(c_areas)
    n_patches = total_area_ha / avg_patch_ha

    se_canopy = abs(c_means[-1]) + (c_stds[-1] / np.sqrt(n_patches))
    se_green = abs(g_means[-1]) + (g_stds[-1] / np.sqrt(n_patches))

    # 6. Plotting
    os.makedirs(os.path.dirname(plot_output_path), exist_ok=True)
    fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(10, 8), sharex=True)
    cumulative_ha = np.cumsum(c_areas)

    ax1.errorbar(cumulative_ha, c_means, yerr=c_stds, fmt='-ob', capsize=5, label='Canopy Mean Error')
    ax1.errorbar(cumulative_ha, g_means, yerr=g_stds, fmt='-og', capsize=5, label='Green Cover Mean Error')
    ax1.set_ylabel("Absolute Error (%)")
    ax1.set_title("Error Convergence")
    ax1.legend()
    ax1.grid(alpha=0.3)

    ax2.plot(cumulative_ha, c_rms, '--*b', label='Canopy RMS Error')
    ax2.plot(cumulative_ha, g_rms, '--*g', label='Green Cover RMS Error')
    ax2.set_ylabel("RMS Error (%)")
    ax2.set_xlabel("Cumulative Testing Area (Hectares)")
    ax2.legend()
    ax2.grid(alpha=0.3)

    plt.tight_layout()
    plt.savefig(plot_output_path, dpi=300, bbox_inches='tight')
    plt.show()

    return {
        'Canopy Cover': c_base_cover, 'Canopy SE': se_canopy,
        'Green Cover': g_base_cover, 'Green SE': se_green,
        'Total Area (Ha)': total_area_ha
    }


