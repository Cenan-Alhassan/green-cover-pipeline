def sample_vectors_to_points(
        points_path: str,
        canopy_vector_dir: str,
        green_vector_dir: str,
        samples_column: str = 'model_prediction',
        output_path: Optional[str] = None
) -> gpd.GeoDataFrame:
    """
    Spatially joins ground truth points with predicted vector polygons.

    Hierarchy Logic:
    - Default: Class 3 (Neither)
    - Intersects Green Vector: Class 2
    - Intersects Canopy Vector: Class 1 (Overwrites Green)

    Inputs:
        points_path: Path to the ground truth points GeoPackage.
        canopy_vector_dir: Directory containing canopy polygons.
        green_vector_dir: Directory containing green cover polygons.
        samples_column: Name of the column to store predictions.
        output_path: Optional path to save the resulting GeoPackage.
    """
    try:
        # 1. Ingest Ground Truth Points
        points = gpd.read_file(points_path)

        # Identify the .shp files in the provided directories
        # Assumes the standard names generated by postprocessing.py
        canopy_shp = os.path.join(canopy_vector_dir, "canopy.shp")
        green_shp = os.path.join(green_vector_dir, "green_cover.shp")

        # Initialize the prediction column with the 'Neither' baseline
        points[samples_column] = 3

        # Store original index to ensure we map joined data back correctly
        points['orig_id'] = points.index

        # 2. Process Green Cover (Class 2)
        if os.path.exists(green_shp):
            green_gdf = gpd.read_file(green_shp)

            # Automatic CRS Alignment
            if points.crs != green_gdf.crs:
                points = points.to_crs(green_gdf.crs)

            # Spatial Join: Points intersecting Green polygons
            joined_green = gpd.sjoin(points, green_gdf, how='inner', predicate='intersects')

            # Update points that were found inside green polygons
            points.loc[joined_green['orig_id'], samples_column] = 2
        else:
            print(f"Warning: Green vector not found at {green_shp}")

        # 3. Process Canopy Cover (Class 1) - THE OVERWRITE STEP
        if os.path.exists(canopy_shp):
            canopy_gdf = gpd.read_file(canopy_shp)

            if points.crs != canopy_gdf.crs:
                points = points.to_crs(canopy_gdf.crs)

            # Spatial Join: Points intersecting Canopy polygons
            joined_canopy = gpd.sjoin(points, canopy_gdf, how='inner', predicate='intersects')

            # Update points. This naturally overrides 2 (Green) with 1 (Canopy)
            points.loc[joined_canopy['orig_id'], samples_column] = 1
        else:
            print(f"Warning: Canopy vector not found at {canopy_shp}")

        # 4. Save results if output_path is provided
        if output_path:
            # Ensure the directory exists
            output_dir = os.path.dirname(output_path)
            if output_dir and not os.path.exists(output_dir):
                os.makedirs(output_dir, exist_ok=True)

            # Save as GeoPackage
            points.to_file(output_path, driver='GPKG')
            print(f"Sampled points successfully saved to: {output_path}")

        # Drop the temporary ID and return
        return points.drop(columns=['orig_id'])
    except Exception as e:
        logger.error(f"Error sampling vectors to points: {e}")
        raise

