def calculate_ukceh_covers(westminster_mask, ukceh_lcm, canopy_classes, green_only_classes):
    # --- 1. Load the Boundary ---
    # Ensure your Westminster vector is in the same CRS as the raster
    westminster = gpd.read_file(westminster_mask)

    # --- 2. Mask the Raster ---
    with rasterio.open(ukceh_lcm) as src:
        # This "clips" the raster data to only the pixels inside the polygon
        out_image, out_transform = mask(src, westminster.geometry, crop=True)
        data = out_image[0]  # Get the first band

    # --- 3. Count Pixels ---
    # Instead of np.sum(data > 0), use the source's metadata to find the real land
    nodata_value = src.nodata
    total_land_pixels = np.sum((data != nodata_value) & (data != 0))

    count_canopy = np.sum(np.isin(data, canopy_classes))
    count_green_only = np.sum(np.isin(data, green_only_classes))

    # Total Green Cover = Canopy + Other Green Vegetation
    count_total_green = count_canopy + count_green_only

    # --- 4. Calculate Percentages ---
    pct_canopy = (count_canopy / total_land_pixels) * 100
