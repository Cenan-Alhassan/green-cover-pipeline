def calculate_ukceh_covers(westminster_mask: str, ukceh_lcm: str, canopy_classes: List[int], green_only_classes: List[int]) -> None:
    """
    Calculates cover percentages from UKCEH LCM data.

    Inputs:
        westminster_mask: Path to the Westminster boundary vector.
        ukceh_lcm: Path to the UKCEH LCM raster.
        canopy_classes: List of class IDs considered canopy.
        green_only_classes: List of class IDs considered other green vegetation.
    """
    try:
        # 1. Load the Boundary
        westminster = gpd.read_file(westminster_mask)

        # 2. Mask the Raster
        with rasterio.open(ukceh_lcm) as src:
            out_image, out_transform = mask(src, westminster.geometry, crop=True)
            data = out_image[0]

        # 3. Count Pixels
        nodata_value = src.nodata
        total_land_pixels = np.sum((data != nodata_value) & (data != 0))

        count_canopy = np.sum(np.isin(data, canopy_classes))
        count_green_only = np.sum(np.isin(data, green_only_classes))

        # Total Green Cover = Canopy + Other Green Vegetation
        count_total_green = count_canopy + count_green_only

        # 4. Calculate Percentages
        pct_canopy = (count_canopy / total_land_pixels) * 100
        pct_total_green = (count_total_green / total_land_pixels) * 100

        print(f"UKCEH Westminster Results:\n")
        print(f"Total Pixels Sampled: {total_land_pixels}")
        print(f"Total Canopy Pixels Sampled: {count_canopy}")
        print(f"Total Green Pixels Sampled: {count_total_green} (includes canopy)\n")
        print(f"Canopy Cover: {pct_canopy:.2f}%")
        print(f"Total Green Cover: {pct_total_green:.2f}%")
    except Exception as e:
        logger.error(f"Error calculating UKCEH covers: {e}")
